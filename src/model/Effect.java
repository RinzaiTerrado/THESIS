package model;

public class Effect {
    String effect;
    String effVal;

    public Effect(String effect){
        this.effect = effect;
    }

    public String getEffect(){
        return effect;
    }

    public void setEffect(String effect){
        this.effect = effect;
    }

    public String getEffVal(){
        return effVal;
    }

    public void setEffVal(String effVal){
        this.effVal = effVal;
    }
}
